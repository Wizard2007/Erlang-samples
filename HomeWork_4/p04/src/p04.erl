-module(p04).

%% API exports
-export([len/1]).

%%====================================================================
%% API functions
%%====================================================================

len(L) -> len(L,0).

len([_|T], C) -> len(T, C+1);
len([],C) -> C.

%%====================================================================
%% Internal functions
%%====================================================================

%%====================================================================
%% Unit tests
%%====================================================================

-ifdef(TEST).
-include_lib("eunit/include/eunit.hrl").
    element_at_test_()->[
        ?_assert(len([])=:=0),
        ?_assert(len([a,b,c,d])=:=4)].
-endif.
