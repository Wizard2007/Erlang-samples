-module(p09).

%% API exports
-export([pack/1]).

%%====================================================================
%% API functions
%%====================================================================

pack(L) -> pack(L, []).

pack([H,H|T], Acc) -> pack([H|T], [H|Acc]);
pack([], Acc) -> Acc;
pack([H|T], Acc) -> [[H|Acc]|pack(T,[])].

